    private void BtnBorrarEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBorrarEmpActionPerformed
        int fila=this.JTableEmp.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(rootPane,
                    "Seleccione un registro de la tabla");
        } else {
            int IDE=Integer.parseInt((String)this.JTableEmp.getValueAt(fila,0).toString());
            DAOEmpleados dao= new DAOEmpleados();
            dao.Eliminar(IDE);
            ObtenerDatos();
        }
    }//GEN-LAST:event_BtnBorrarEmpActionPerformed

