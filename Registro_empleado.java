    private void BtnAgregarEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarEmpActionPerformed
        String nomempl=TxtNombreEmp.getText();
        String apellempl=TxtApellidoEmp.getText();
        String carempl=TxtCargoEmp.getText();
        String telempl=TxtTelefonoEmp.getText();
        if (nomempl.contentEquals("")||apellempl.contentEquals("")||
                carempl.contentEquals("")||telempl.contentEquals("")){
            JOptionPane.showMessageDialog(rootPane,
                    "Todos los campos son obligatorios");
        }else{
            try{
                Empleados au=new DAOEmpleados().Insertar(nomempl,
                        apellempl, carempl, telempl);
                JOptionPane.showMessageDialog(rootPane, "Registro agregado");
            }catch (Exception e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(rootPane, "No se agrego el resgistro");
            }
        }
        ObtenerDatos();
        LimpiarCampos();
    }//GEN-LAST:event_BtnAgregarEmpActionPerformed

    private void BtnEditarEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditarEmpActionPerformed
        int fila=this.JTableEmp.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(rootPane,
                    "Seleccione un registro de la tabla");
        } else{
            try{
            int IDE=Integer.parseInt((String)this.JTableEmp.getValueAt(fila,0).toString());
            String nomemp=(String)this.JTableEmp.getValueAt(fila, 1);
            String apellemp=(String)this.JTableEmp.getValueAt(fila,2);
            String caremp=(String)this.JTableEmp.getValueAt(fila,3);
            String telemp=(String)this.JTableEmp.getValueAt(fila,4);
            
            TxtIDEmp.setText(""+IDE);
            TxtNombreEmp.setText(nomemp);
            TxtApellidoEmp.setText(apellemp);
            TxtCargoEmp.setText(caremp);
            TxtTelefonoEmp.setText(telemp);
        }catch(NumberFormatException e){
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_BtnEditarEmpActionPerformed

