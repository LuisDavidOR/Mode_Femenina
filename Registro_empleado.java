     public void ObtenerDatos(){
        List<Empleados> empleado =new DAOEmpleados().ObtenerDatos();
        DefaultTableModel modelo = new DefaultTableModel();
        String[] columnas={"ID_Empleado","Nom_empl",
            "Apell_empl","Cargo_empl","Tel_empl"};
        modelo.setColumnIdentifiers(columnas);
        for(Empleados au:empleado){
            String[]renglon={Integer.toString(au.getID_Empleado()),au.getNom_empl(),
                au.getApell_empl(), au.getCargo_empl(), au.getTel_empl()};
            modelo.addRow(renglon);
        }
        JTableEmp.setModel(modelo);
    }
     
     public void actualizarEmpleados(){
                int IDE= Integer.parseInt(this.TxtIDEmp.getText());
        String nomemp=this.TxtNombreEmp.getText();
        String apellemp=this.TxtApellidoEmp.getText();
        String caremp=this.TxtCargoEmp.getText();
        String telemp=this.TxtTelefonoEmp.getText();
        
        DAOEmpleados dao=new DAOEmpleados();
        int res=dao.Actualizar(IDE,nomemp,
                apellemp,caremp,telemp);
        if(res==1){
            JOptionPane.showMessageDialog(rootPane,"¡Empleado actualizado!");
        } else {
            JOptionPane.showMessageDialog(rootPane,"¡Ocurrió un ERROR!");
        }
    }
     
     public void BuscarEmpleados(List <Empleados> empleado){
            DefaultTableModel modelo = new DefaultTableModel();
            
            String[] columnas = {"ID_cliente", "Nom_cli", "Apell_cli",
                "Tel_cli", "Fec_reg_cli"};
            modelo.setColumnIdentifiers(columnas);
            
            for(Empleados emp: empleado){
                String[] renglon = {Integer.toString(emp.getID_Empleado()),
                    emp.getNom_empl(),
                    emp.getApell_empl(),
                    emp.getCargo_empl(),
                    emp.getTel_empl()};
                
                modelo.addRow(renglon);
            }
            JTableEmp.setModel(modelo);
        }

