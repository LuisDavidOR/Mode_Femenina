    private void BtnBuscarEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarEmpActionPerformed
        String terminoBusqueda = TxtBuscarEmp.getText().trim();
        
        if(!terminoBusqueda.isEmpty()){
            List<Empleados> resultados = new DAOEmpleados().Buscar(terminoBusqueda);
            BuscarEmpleados(resultados);
        }else {
            JOptionPane.showMessageDialog(null,
                    "Porfavor ingrese algo para buscar","Advertencia",
                    JOptionPane.WARNING_MESSAGE);
            ObtenerDatos();
        }
    }//GEN-LAST:event_BtnBuscarEmpActionPerformed


